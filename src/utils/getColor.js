export const getColor = (currentPath) => {
  let result = {};

  if (currentPath === "/planeacion") {
    result = {
      hex: "#D93535",
      border: "border-primary-red-600",
      bg: "bg-primary-red-600",
    };
  } else if (currentPath === "/solicitudes") {
    result = {
      hex: "#FAA500",
      border: "border-primary-yellow-600",
      bg: "bg-primary-yellow-600",
    };
  }

  return result;
};
